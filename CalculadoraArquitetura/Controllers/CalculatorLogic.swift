import Foundation

class CalculatorLogic {
    func sum(_ a: Int, _ b: Int) -> Int {
        return a+b
    }
    
    func sub(_ a: Int, _ b: Int) -> Int {
        return a-b
    }
    
    func mult(_ a: Int, _ b: Int) -> Int {
        return a*b
    }
    
    func div(_ a: Int, _ b: Int) -> Int {
        return a/b
    }
}
